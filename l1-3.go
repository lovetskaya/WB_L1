// Дана последовательность чисел: 2,4,6,8,10.
//Найти сумму их квадратов(22+32+42….) с использованием конкурентных вычислений.

package main

import (
	"fmt"
	"sync"
)

func main() {
	numbers := []int{2, 4, 6, 8, 10}

	// Создаем WaitGroup для ожидания завершения всех горутин
	var wg sync.WaitGroup

	results := make(chan int) // Канал для передачи результатов

	// Запускаем горутины для расчета квадратов
	for _, num := range numbers {
		wg.Add(1) // Увеличиваем счетчик WaitGroup
		go func(n int) {
			defer wg.Done() // Уменьшаем счетчик по завершении
			results <- n * n // Отправляем квадрат в канал
		}(num)
	}

	// Запускаем горутину для закрытия канала после завершения всех расчетов
	go func() {
		wg.Wait() // Ждем завершения всех горутин
		close(results) // Закрываем канал
	}()

	// Суммируем результаты из канала
	sum := 0
	for square := range results {
		sum += square
	}

	// Выводим результат
	fmt.Println("Сумма квадратов:", sum)
}
